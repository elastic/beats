package module

import (
	"fmt"
	"sync"

	"github.com/elastic/beats/libbeat/beat"
	"github.com/elastic/beats/libbeat/monitoring"
)

var (
	moduleList     map[string]int
	moduleListLock sync.Mutex
)

func init() {
	moduleList = map[string]int{}
	monitoring.NewFunc(monitoring.GetNamespace("state").GetRegistry(), "module", reportModules, monitoring.Report)
}

// Runner is a facade for a Wrapper that provides a simple interface
// for starting and stopping a Module.
type Runner interface {
	// Start starts the Module. If Start is called more than once, only the
	// first will start the Module.
	Start()

	// Stop stops the Module and waits for module's MetricSets to exit. The
	// publisher.Client will be closed by Stop. If Stop is called more than
	// once, only the first stop the Module and wait for it to exit.
	Stop()
}

// NewRunner returns a Runner facade. The events generated by
// the Module will be published to a new publisher.Client generated from the
// pubClientFactory.
func NewRunner(client beat.Client, mod *Wrapper) Runner {
	return &runner{
		done:   make(chan struct{}),
		mod:    mod,
		client: client,
	}
}

type runner struct {
	done      chan struct{}
	wg        sync.WaitGroup
	startOnce sync.Once
	stopOnce  sync.Once
	mod       *Wrapper
	client    beat.Client
}

func (mr *runner) Start() {
	mr.startOnce.Do(func() {
		output := mr.mod.Start(mr.done)
		mr.wg.Add(1)
		addModule(mr.mod.Name())
		go func() {
			defer mr.wg.Done()
			PublishChannels(mr.client, output)
		}()
	})
}

func (mr *runner) Stop() {
	mr.stopOnce.Do(func() {
		close(mr.done)
		mr.client.Close()
		mr.wg.Wait()
		removeModule(mr.mod.Name())
	})
}

func (mr *runner) String() string {
	return fmt.Sprintf("%s [metricsets=%d]", mr.mod.Name(), len(mr.mod.metricSets))
}

func addModule(module string) {
	moduleListLock.Lock()
	defer moduleListLock.Unlock()
	moduleList[module]++
}

func removeModule(module string) {
	moduleListLock.Lock()
	defer moduleListLock.Unlock()
	moduleList[module]--
}

func reportModules(m monitoring.Mode, V monitoring.Visitor) {
	V.OnRegistryStart()
	defer V.OnRegistryFinished()

	var modules []string
	var count int64

	moduleListLock.Lock()
	defer moduleListLock.Unlock()

	for key, val := range moduleList {
		if val > 0 {
			modules = append(modules, key)
		}
		count += int64(val)
	}

	monitoring.ReportInt(V, "count", count)
	monitoring.ReportStringSlice(V, "names", modules)
}
